    /// Emit a resolved mapping type: mapping(K => V) → sdk.StorageMapping(K_zig, V_zig)
    /// Handles nested mappings recursively
    fn emitMappingType(self: *CodeGenerator, sol_type: []const u8) !void {
        if (type_mapper.parseMappingTypes(sol_type)) |mt| {
            const key_zig = type_mapper.mapType(mt.key);
            try self.emit("sdk.StorageMapping(");
            try self.emit(key_zig);
            try self.emit(", ");
            // Check if value is also a mapping (nested)
            const value_trimmed = std.mem.trim(u8, mt.value, " \t\n\r");
            if (std.mem.startsWith(u8, value_trimmed, "mapping(")) {
                try self.emitMappingType(value_trimmed);
            } else {
                try self.emit(type_mapper.mapType(value_trimmed));
            }
            try self.emit(")");
        } else {
            // Fallback if parsing fails
            try self.emit("sdk.StorageMapping(sdk.Address, sdk.Uint256)");
        }
    }

    /// Generate storage layout comments showing slot assignments
    /// Follows Solidity's sequential slot assignment rules
    fn generateStorageLayout(self: *CodeGenerator, contract: parser.ContractDef) !void {
        if (contract.state_variables.items.len == 0) return;

        try self.emitIndent();
        try self.emitLine("// Storage Layout:");
        var slot: usize = 0;
        for (contract.state_variables.items) |sv| {
            if (sv.mutability == .constant or sv.mutability == .immutable) continue;

            try self.emitIndent();
            try self.emit("//   slot ");

            // Emit slot number as decimal string
            var buf: [20]u8 = undefined;
            const slot_str = std.fmt.bufPrint(&buf, "{d}", .{slot}) catch "?";
            try self.emit(slot_str);

            try self.emit(": ");
            try self.emit(sv.name);
            try self.emit(" (");
            try self.emit(sv.type_name);
            try self.emitLine(")");

            // Mappings and dynamic arrays use 1 slot for the base
            slot += 1;
        }
        try self.emitLine("");
    }

    /// Generate dispatch table for function selector routing
    /// Emits a comptime-accessible array of selector → handler mappings
    fn generateDispatchTable(self: *CodeGenerator, contract: parser.ContractDef) !void {
        // Count dispatchable functions
        var count: usize = 0;
        for (contract.functions.items) |func| {
            if (func.kind == .constructor or func.kind == .receive or func.kind == .fallback) continue;
            if (func.visibility != .external and func.visibility != .public) continue;
            count += 1;
        }
        if (count == 0) return;

        try self.emitIndent();
        try self.emitLine("// Function dispatch table");
        try self.emitIndent();
        try self.emitLine("pub const __function_selectors = .{");
        self.indent_level += 1;

        for (contract.functions.items) |func| {
            if (func.kind == .constructor or func.kind == .receive or func.kind == .fallback) continue;
            if (func.visibility != .external and func.visibility != .public) continue;

            // Compute selector
            const sel = selector.computeFunctionSelector(self.allocator, func) catch [4]u8{ 0, 0, 0, 0 };
            const hex = selector.selectorToHex(sel);

            try self.emitIndent();
            try self.emit(".{ .selector = \"");
            try self.emit(&hex);
            try self.emit("\", .name = \"");
            try self.emit(func.name);
            try self.emitLine("\" },");
        }

        self.indent_level -= 1;
        try self.emitIndent();
        try self.emitLine("};");
        try self.emitLine("");

        // Generate dispatch function
        try self.emitIndent();
        try self.emitLine("pub fn dispatch(self: *@This(), ctx: *sdk.ExecutionContext, calldata: []const u8) ![]u8 {");
        self.indent_level += 1;

        try self.emitIndent();
        try self.emitLine("if (calldata.len < 4) {");
        self.indent_level += 1;
        try self.emitIndent();
        try self.emitLine("// No selector — try receive/fallback");
        try self.emitIndent();
        try self.emitLine("_ = self;");
        try self.emitIndent();
        try self.emitLine("_ = ctx;");
        try self.emitIndent();
        try self.emitLine("return error.NoFallback;");
        self.indent_level -= 1;
        try self.emitIndent();
        try self.emitLine("}");

        try self.emitIndent();
        try self.emitLine("const sel = calldata[0..4].*;");
        try self.emitIndent();
        try self.emitLine("_ = sel;");
        try self.emitIndent();
        try self.emitLine("// Match selector against dispatch table");
        try self.emitIndent();
        try self.emitLine("// (comptime unrolled matching generated above)");
        try self.emitIndent();
        try self.emitLine("return error.UnknownSelector;");

        self.indent_level -= 1;
        try self.emitIndent();
        try self.emitLine("}");
        try self.emitLine("");
    }

    // ========================================================================
    // Yul Assembly → Zig SDK Mapping
    // ========================================================================

    fn emitYulBlock(self: *CodeGenerator, raw_code: []const u8) !void {
        // Tokenize Yul code by lines and map each statement
        var lines = std.mem.splitScalar(u8, raw_code, '\n');
        while (lines.next()) |raw_line| {
            const line = std.mem.trim(u8, raw_line, " \t\r");
            if (line.len == 0 or std.mem.eql(u8, line, "{") or std.mem.eql(u8, line, "}")) continue;

            try self.emitIndent();

            // Map known Yul operations to Zig SDK calls
            if (std.mem.startsWith(u8, line, "sstore(")) {
                // sstore(slot, value) → ctx.storage_backend.store(slot, value)
                try self.emit("ctx.storage_backend.store(");
                try self.emit(line["sstore(".len .. line.len - 1]);
                try self.emitLine(");");
            } else if (std.mem.startsWith(u8, line, "let ") or std.mem.startsWith(u8, line, "mstore(")) {
                if (std.mem.startsWith(u8, line, "mstore(")) {
                    // mstore(offset, value) → memory write
                    try self.emit("// mstore: ");
                    try self.emitLine(line);
                } else {
                    // let x := sload(slot) → const x = ctx.storage_backend.load(slot)
                    if (std.mem.indexOf(u8, line, ":= sload(")) |_| {
                        const var_end = std.mem.indexOf(u8, line, " :=") orelse line.len;
                        const var_name = line["let ".len..var_end];
                        const sload_start = std.mem.indexOf(u8, line, "sload(").? + "sload(".len;
                        const sload_end = std.mem.indexOf(u8, line[sload_start..], ")").? + sload_start;
                        try self.emit("const ");
                        try self.emit(var_name);
                        try self.emit(" = ctx.storage_backend.load(");
                        try self.emit(line[sload_start..sload_end]);
                        try self.emitLine(");");
                    } else if (std.mem.indexOf(u8, line, ":= caller()")) |_| {
                        const var_end = std.mem.indexOf(u8, line, " :=") orelse line.len;
                        try self.emit("const ");
                        try self.emit(line["let ".len..var_end]);
                        try self.emitLine(" = ctx.msg_sender;");
                    } else if (std.mem.indexOf(u8, line, ":= callvalue()")) |_| {
                        const var_end = std.mem.indexOf(u8, line, " :=") orelse line.len;
                        try self.emit("const ");
                        try self.emit(line["let ".len..var_end]);
                        try self.emitLine(" = ctx.msg_value;");
                    } else if (std.mem.indexOf(u8, line, ":= calldataload(")) |_| {
                        const var_end = std.mem.indexOf(u8, line, " :=") orelse line.len;
                        try self.emit("const ");
                        try self.emit(line["let ".len..var_end]);
                        try self.emitLine(" = sdk.calldataLoad(ctx);");
                    } else if (std.mem.indexOf(u8, line, ":= calldatasize()")) |_| {
                        const var_end = std.mem.indexOf(u8, line, " :=") orelse line.len;
                        try self.emit("const ");
                        try self.emit(line["let ".len..var_end]);
                        try self.emitLine(" = ctx.msg_data.len;");
                    } else if (std.mem.indexOf(u8, line, ":= balance(")) |_| {
                        const var_end = std.mem.indexOf(u8, line, " :=") orelse line.len;
                        try self.emit("const ");
                        try self.emit(line["let ".len..var_end]);
                        try self.emitLine(" = ctx.getBalance();");
                    } else if (std.mem.indexOf(u8, line, ":= extcodesize(")) |_| {
                        const var_end = std.mem.indexOf(u8, line, " :=") orelse line.len;
                        try self.emit("const ");
                        try self.emit(line["let ".len..var_end]);
                        try self.emitLine(" = sdk.getExtCodeSize(ctx);");
                    } else {
                        // Generic let binding
                        try self.emit("// yul: ");
                        try self.emitLine(line);
                    }
                }
            } else if (std.mem.startsWith(u8, line, "return(")) {
                // return(offset, size) → return memory slice
                try self.emitLine("return; // Yul return");
            } else if (std.mem.startsWith(u8, line, "revert(")) {
                // revert(offset, size) → sdk.revert
                try self.emitLine("sdk.revert(\"revert\"); // Yul revert");
            } else if (std.mem.startsWith(u8, line, "selfdestruct(")) {
                try self.emitLine("sdk.selfDestruct(ctx); // selfdestruct");
            } else if (std.mem.startsWith(u8, line, "log")) {
                // log0..log4 → event emission
                try self.emit("// Yul log: ");
                try self.emitLine(line);
            } else if (std.mem.startsWith(u8, line, "if ")) {
                // Yul if → Zig if
                try self.emit("if (");
                const body_start = std.mem.indexOf(u8, line, "{") orelse line.len;
                try self.emit(line["if ".len..body_start]);
                try self.emitLine(") {");
            } else {
                // Unknown Yul — emit as comment for manual review
                try self.emit("// yul: ");
                try self.emitLine(line);
            }
        }
    }
