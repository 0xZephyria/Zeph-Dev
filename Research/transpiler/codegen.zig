    fn emitRawBody(self: *CodeGenerator, body: []const u8) !void {
        var trimmed = body;
        if (trimmed.len > 0 and trimmed[0] == '{') trimmed = trimmed[1..];
        if (trimmed.len > 0 and trimmed[trimmed.len - 1] == '}') trimmed = trimmed[0 .. trimmed.len - 1];
        trimmed = std.mem.trim(u8, trimmed, " \t\n\r");
        if (trimmed.len == 0) {
            try self.emitUnused("self");
            try self.emitUnused("ctx");
            return;
        }
        try self.emitIndent();
        try self.emitLine("// TODO: Transpile function body (parse error fallback)");
        var lines = std.mem.splitSequence(u8, trimmed, "\n");
        while (lines.next()) |line| {
            const l = std.mem.trim(u8, line, " \t\r");
            if (l.len == 0) continue;
            try self.emitIndent();
            try self.emit("// ");
            try self.emitLine(l);
        }
    }

    fn emitRawExpression(self: *CodeGenerator, expr: []const u8) !void {
        try self.emit(expr);
    }

    /// Emit a Uint256 constant value from a raw Solidity literal.
    /// Handles: plain integers ("7"), scientific notation ("69e9"), hex ("0xff").
    fn emitUint256Constant(self: *CodeGenerator, val: []const u8) !void {
        const trimmed = std.mem.trim(u8, val, " \t\r\n");

        // Check for scientific notation: NUMeEXP (e.g., "69e9", "1e18")
        if (std.mem.indexOfScalar(u8, trimmed, 'e')) |e_pos| {
            // Parse mantissa and exponent
            const mantissa_str = trimmed[0..e_pos];
            const exp_str = trimmed[e_pos + 1 ..];
            const mantissa = std.fmt.parseInt(u64, mantissa_str, 10) catch {
                // Fallback
                try self.emit("Uint256.fromU64(");
                try self.emit(trimmed);
                try self.emit(")");
                return;
            };
            const exponent = std.fmt.parseInt(u32, exp_str, 10) catch {
                try self.emit("Uint256.fromU64(");
                try self.emit(trimmed);
                try self.emit(")");
                return;
            };

            // Compute value: mantissa * 10^exponent
            // For values that fit in u64 (< 1.8e19), use fromU64
            // For larger values, use fromU128 or chain multiply
            if (exponent <= 19) {
                var power: u128 = 1;
                for (0..exponent) |_| power *= 10;
                const result = @as(u128, mantissa) * power;
                if (result <= std.math.maxInt(u64)) {
                    try self.emit("Uint256.fromU64(");
                    var num_buf: [20]u8 = undefined;
                    const num_str = std.fmt.bufPrint(&num_buf, "{d}", .{@as(u64, @intCast(result))}) catch "0";
                    try self.emit(num_str);
                    try self.emit(")");
                } else if (result <= std.math.maxInt(u128)) {
                    try self.emit("Uint256.fromU128(");
                    var num_buf: [40]u8 = undefined;
                    const num_str = std.fmt.bufPrint(&num_buf, "{d}", .{result}) catch "0";
                    try self.emit(num_str);
                    try self.emit(")");
                } else {
                    try self.emit("Uint256.fromU64(");
                    try self.emit(mantissa_str);
                    try self.emit(").checkedMul(Uint256.fromU64(10).exp(Uint256.fromU64(");
                    try self.emit(exp_str);
                    try self.emit(")))");
                }
            } else {
                // Very large exponent — use runtime computation
                try self.emit("Uint256.fromU64(");
                try self.emit(mantissa_str);
                try self.emit(").checkedMul(Uint256.fromU64(10).exp(Uint256.fromU64(");
                try self.emit(exp_str);
                try self.emit(")))");
            }
            return;
        }

        // Check for hex: 0x...
        if (trimmed.len >= 2 and trimmed[0] == '0' and (trimmed[1] == 'x' or trimmed[1] == 'X')) {
            try self.emit("Uint256.fromHex(\"");
            try self.emit(trimmed);
            try self.emit("\")");
            return;
        }

        // Plain integer literal
        // Check if it fits in u64
        if (std.fmt.parseInt(u64, trimmed, 10)) |_| {
            try self.emit("Uint256.fromU64(");
            try self.emit(trimmed);
            try self.emit(")");
        } else |_| {
            // Try u128
            if (std.fmt.parseInt(u128, trimmed, 10)) |_| {
                try self.emit("Uint256.fromU128(");
                try self.emit(trimmed);
                try self.emit(")");
            } else |_| {
                // Fallback: emit raw
                try self.emit(trimmed);
            }
        }
    }

    fn emitParamName(self: *CodeGenerator, name: []const u8) !void {
        if (name.len > 0) try self.emit(name) else try self.emit("_");
    }

    fn emitUnused(self: *CodeGenerator, name: []const u8) !void {
        try self.emitIndent();
        try self.emit("_ = ");
        try self.emit(name);
        try self.emitLine(";");
    }

    /// Check if identifier is a state variable name in the current contract
    fn isStateVar(self: *const CodeGenerator, name: []const u8) bool {
        for (self.current_state_vars) |sv_name| {
            if (std.mem.eql(u8, name, sv_name)) return true;
        }
        return false;
    }

    /// Check if a state variable is a StorageSlot type (not mapping/array/string/bytes)
    /// Only StorageSlot vars need .load()/.store() — mappings use .get()/.set() directly
    fn isSlotStateVar(self: *const CodeGenerator, name: []const u8) bool {
        for (self.current_state_vars, 0..) |sv_name, i| {
            if (std.mem.eql(u8, name, sv_name)) {
                if (i < self.current_state_var_types.len) {
                    const sol_type = self.current_state_var_types[i];
                    // Mapping, array, string, bytes are NOT slot types
                    if (std.mem.startsWith(u8, sol_type, "mapping(") or
                        std.mem.startsWith(u8, sol_type, "mapping ") or
                        std.mem.indexOf(u8, sol_type, "[") != null or
                        std.mem.eql(u8, sol_type, "string") or
                        std.mem.eql(u8, sol_type, "bytes"))
                    {
                        return false;
                    }
                }
                return true;
            }
        }
        return false;
    }

    /// Check if an expression target refers to a storage location
    /// (state variable, index into state variable mapping)
    fn isStorageTarget(self: *const CodeGenerator, expr: ast.Expression) bool {
        return switch (expr) {
            .identifier => |id| self.isStateVar(id.name),
            .state_var_ref => true,
            .index_access => |ia| self.isStorageTarget(ia.object),
            .member_access => |ma| self.isStorageTarget(ma.object),
            else => false,
        };
    }

    /// Check if a name matches a function in the current contract
    fn isContractFunction(self: *const CodeGenerator, name: []const u8) bool {
        for (self.current_contract_functions) |fn_name| {
            if (std.mem.eql(u8, name, fn_name)) return true;
        }
        return false;
    }

    /// Emit the SDK method call suffix for compound assignment operators
    /// e.g., += becomes .checkedAdd(, -= becomes .checkedSub(
    fn emitCompoundOp(self: *CodeGenerator, op: ast.AssignmentOp) !void {
        switch (op) {
            .add_assign => try self.emit(".checkedAdd("),
            .sub_assign => try self.emit(".checkedSub("),
            .mul_assign => try self.emit(".checkedMul("),
            .div_assign => try self.emit(".checkedDiv("),
            .mod_assign => try self.emit(".mod("),
            .or_assign => try self.emit(".bitwiseOr("),
            .and_assign => try self.emit(".bitwiseAnd("),
            .xor_assign => try self.emit(".bitwiseXor("),
            .shl_assign => try self.emit(".shl("),
            .shr_assign => try self.emit(".shr("),
            .assign => {}, // Should not reach here
        }
    }

    fn emit(self: *CodeGenerator, text: []const u8) !void {
        try self.output.appendSlice(self.allocator, text);
    }

    fn emitLine(self: *CodeGenerator, text: []const u8) !void {
        try self.output.appendSlice(self.allocator, text);
        try self.output.append(self.allocator, '\n');
    }

    fn emitIndent(self: *CodeGenerator) !void {
        var i: usize = 0;
        while (i < self.indent_level) : (i += 1) {
            try self.output.appendSlice(self.allocator, "    ");
        }
    }

