// File: tools/sol2zig/codegen.zig
// Zig source code generator from Solidity AST
// Produces Zephyria SDK-compatible Zig source with full expression/statement transpilation

const std = @import("std");
const parser = @import("parser.zig");
const type_mapper = @import("type_mapper.zig");
const ast = @import("ast.zig");
const ExprParser = @import("expr_parser.zig").ExprParser;
const selector = @import("selector.zig");

pub const CodeGenerator = struct {
    allocator: std.mem.Allocator,
    parsed_ast: *const parser.SolidityAST,
    type_map: []const type_mapper.TypeMapping,
    output: std.ArrayListUnmanaged(u8),
    indent_level: usize,
    /// Names of current contract's state variables (set during generateContract)
    current_state_vars: []const []const u8,
    /// Solidity types of current contract's state variables (parallel to current_state_vars)
    current_state_var_types: []const []const u8,
    /// True when emitting the LHS of an assignment (suppress .load() for state vars)
    emitting_assign_target: bool,
    /// Track already-emitted top-level custom error names to avoid duplicates
    emitted_errors: std.StringHashMap(void),
    /// Names of functions in the current contract (for detecting internal calls)
    current_contract_functions: []const []const u8,
    /// Number of contracts from the original source (before imports were appended)
    original_contract_count: usize,
    /// Parameter renames for the current function (original -> renamed)
    /// Used to avoid Zig parameter shadowing of struct function names
    param_renames: std.StringHashMap([]const u8),

    pub fn init(
        allocator: std.mem.Allocator,
        parsed_ast: *const parser.SolidityAST,
        type_map: []const type_mapper.TypeMapping,
    ) CodeGenerator {
        return .{
            .allocator = allocator,
            .parsed_ast = parsed_ast,
            .type_map = type_map,
            .output = .{},
            .indent_level = 0,
            .current_state_vars = &.{},
            .current_state_var_types = &.{},
            .emitting_assign_target = false,
            .emitted_errors = std.StringHashMap(void).init(allocator),
            .current_contract_functions = &.{},
            .original_contract_count = 0,
            .param_renames = std.StringHashMap([]const u8).init(allocator),
        };
    }

    pub fn deinit(self: *CodeGenerator) void {
        self.output.deinit(self.allocator);
        self.emitted_errors.deinit();
        // Free duped rename strings
        var it = self.param_renames.valueIterator();
        while (it.next()) |val| {
            self.allocator.free(val.*);
        }
        self.param_renames.deinit();
    }

    pub fn generate(self: *CodeGenerator) ![]const u8 {
        try self.emitLine("// Auto-generated by sol2zig — Zephyria Solidity-to-Zig Transpiler");
        try self.emitLine("// Do not edit manually");
        try self.emitLine("");
        try self.emitLine("const sdk = @import(\"zephyria-sdk\");");
        try self.emitLine("const Uint256 = sdk.Uint256;");
        try self.emitLine("const Int256 = sdk.Int256;");
        try self.emitLine("const Address = sdk.Address;");
        try self.emitLine("const Bytes32 = sdk.Bytes32;");
        try self.emitLine("");

        // Only emit the main contracts from the original source file.
        // Imported base contracts (ERC20, Ownable, etc.) are merged into the
        // main contract via inheritance and should not be emitted separately.
        const count = if (self.original_contract_count > 0)
            @min(self.original_contract_count, self.parsed_ast.contracts.items.len)
        else
            self.parsed_ast.contracts.items.len;
        for (self.parsed_ast.contracts.items[0..count]) |contract| {
            try self.generateContract(contract);
            try self.emitLine("");
        }

        return self.output.items;
    }

    fn generateContract(self: *CodeGenerator, contract: parser.ContractDef) !void {
        try self.emit("// Contract: ");
        try self.emitLine(contract.name);

        for (contract.enums.items) |e| try self.generateEnum(e);
        for (contract.structs.items) |s| try self.generateStruct(s);
        for (contract.errors_list.items) |err| try self.generateCustomError(err);

        try self.emit("pub const ");
        try self.emit(contract.name);
        try self.emitLine(" = struct {");
        self.indent_level += 1;

        // Collect state variable names and types for self-prefixing in function bodies
        var sv_names: std.ArrayListUnmanaged([]const u8) = .{};
        defer sv_names.deinit(self.allocator);
        var sv_types: std.ArrayListUnmanaged([]const u8) = .{};
        defer sv_types.deinit(self.allocator);
        for (contract.state_variables.items) |sv| {
            if (sv.mutability != .constant) {
                try sv_names.append(self.allocator, sv.name);
                try sv_types.append(self.allocator, sv.type_name);
            }
        }
        self.current_state_vars = sv_names.items;
        self.current_state_var_types = sv_types.items;

        // Collect function names for internal call detection
        var fn_names: std.ArrayListUnmanaged([]const u8) = .{};
        defer fn_names.deinit(self.allocator);
        for (contract.functions.items) |func| {
            if (func.name.len > 0) {
                try fn_names.append(self.allocator, func.name);
            }
        }
        self.current_contract_functions = fn_names.items;

        // State variables
        for (contract.state_variables.items) |sv| try self.generateStateVariable(sv);
        if (contract.state_variables.items.len > 0) try self.emitLine("");

        // Events
        for (contract.events.items) |event| try self.generateEvent(event);
        if (contract.events.items.len > 0) try self.emitLine("");

        // Functions
        for (contract.functions.items) |func| {
            switch (func.kind) {
                .constructor => try self.generateConstructor(func),
                .receive => try self.generateSpecialFunction("receive", func),
                .fallback => try self.generateSpecialFunction("fallback", func),
                .function => try self.generateFunction(func),
            }
            try self.emitLine("");
        }

        // Note: Auto-generated getters removed — field declarations serve as schema
        // Access state via self.field.load(ctx.storage_backend) / self.field.store(ctx.storage_backend, val)

        // Phase 5: Storage layout comment
        try self.generateStorageLayout(contract);

        // Phase 5: Dispatch table
        try self.generateDispatchTable(contract);

        self.indent_level -= 1;
        try self.emitLine("};");
    }

    fn generateStateVariable(self: *CodeGenerator, sv: parser.StateVarDef) !void {
        try self.emitIndent();
        if (sv.mutability == .constant) {
            try self.emit("pub const ");
            try self.emit(sv.name);
            try self.emit(": ");
            try self.emit(type_mapper.mapType(sv.type_name));
            if (sv.initial_value) |val| {
                try self.emit(" = ");
                // Wrap numeric constant values for Uint256/Int256 types
                const mapped_type = type_mapper.mapType(sv.type_name);
                if (std.mem.eql(u8, mapped_type, "sdk.Uint256") or std.mem.eql(u8, mapped_type, "Uint256")) {
                    try self.emitUint256Constant(val);
                } else if (std.mem.eql(u8, mapped_type, "sdk.Int256") or std.mem.eql(u8, mapped_type, "Int256")) {
                    try self.emitUint256Constant(val);
                } else {
                    try self.emitRawExpression(val);
                }
            }
            try self.emitLine(";");
        } else {
            try self.emit(sv.name);
            try self.emit(": ");
            // Resolve mapping types to sdk.StorageMapping(K, V)
            const storage_type = type_mapper.mapStorageType(sv.type_name);
            if (std.mem.eql(u8, storage_type, "Mapping")) {
                try self.emitMappingType(sv.type_name);
            } else {
                try self.emit(storage_type);
            }
            try self.emitLine(",");
        }
    }

    fn generateEvent(self: *CodeGenerator, event: parser.EventDef) !void {
        try self.emitIndent();
        try self.emit("pub const ");
        try self.emit(event.name);
        try self.emit(" = sdk.Event(\"");
        try self.emit(event.name);
        try self.emit("\", .{");
        for (event.params.items, 0..) |param, i| {
            if (i > 0) try self.emit(", ");
            try self.emit("\n");
            try self.emitIndent();
            try self.emit("    .{ .name = \"");
            try self.emit(param.name);
            try self.emit("\", .type_name = \"");
            try self.emit(type_mapper.mapType(param.type_name));
            try self.emit("\", .indexed = ");
            if (param.is_indexed) try self.emit("true") else try self.emit("false");
            try self.emit(" }");
        }
        try self.emitLine(",");
        try self.emitIndent();
        try self.emitLine("});");
    }

    fn generateConstructor(self: *CodeGenerator, ctor: parser.FunctionDef) !void {
        try self.emitIndent();
        try self.emit("pub fn init(self: *@This(), ctx: *sdk.ExecutionContext");
        for (ctor.params.items) |param| {
            try self.emit(", ");
            try self.emitParamName(param.name);
            try self.emit(": ");
            try self.emit(type_mapper.mapType(param.type_name));
        }
        try self.emitLine(") void {");
        self.indent_level += 1;
        if (ctor.body) |body| {
            try self.emitTranspiledBody(body);
        } else {
            try self.emitUnused("self");
            try self.emitUnused("ctx");
        }
        self.indent_level -= 1;
        try self.emitIndent();
        try self.emitLine("}");
    }

    fn generateFunction(self: *CodeGenerator, func: parser.FunctionDef) !void {
        // Clear param renames from previous function, freeing duped strings
        {
            var it = self.param_renames.valueIterator();
            while (it.next()) |val| {
                self.allocator.free(val.*);
            }
            self.param_renames.clearRetainingCapacity();
        }

        try self.emitIndent();
        try self.emit("pub fn ");
        try self.emit(func.name);
        try self.emit("(self: *@This(), ctx: *sdk.ExecutionContext");
        for (func.params.items) |param| {
            try self.emit(", ");
            // Rename params that shadow function names in the same contract
            const param_name = if (param.name.len > 0) param.name else "_";
            if (self.isContractFunction(param_name)) {
                // Build renamed param using arena-duped buffer
                var rename_buf: [128]u8 = undefined;
                const renamed = std.fmt.bufPrint(&rename_buf, "{s}_param", .{param_name}) catch param_name;
                const renamed_dupe = self.allocator.dupe(u8, renamed) catch param_name;
                try self.param_renames.put(param_name, renamed_dupe);
                try self.emit(renamed_dupe);
            } else {
                try self.emit(param_name);
            }
            try self.emit(": ");
            try self.emit(type_mapper.mapType(param.type_name));
        }
        try self.emit(") ");

        // Return type
        if (func.returns.items.len == 0) {
            try self.emit("void");
        } else if (func.returns.items.len == 1) {
            try self.emit(type_mapper.mapType(func.returns.items[0].type_name));
        } else {
            try self.emit("struct { ");
            for (func.returns.items, 0..) |ret, i| {
                if (i > 0) try self.emit(", ");
                if (ret.name.len > 0) {
                    try self.emit(ret.name);
                } else {
                    try self.emit(std.fmt.allocPrint(self.allocator, "_{d}", .{i}) catch "val");
                }
                try self.emit(": ");
                try self.emit(type_mapper.mapType(ret.type_name));
            }
            try self.emit(" }");
        }

        try self.emitLine(" {");
        self.indent_level += 1;

        // Emit modifier guards (inline the modifier body)
        for (func.modifiers_applied.items) |mod_call| {
            try self.emitIndent();
            // Map well-known modifiers to their guard functions
            if (std.mem.eql(u8, mod_call.name, "onlyOwner")) {
                try self.emitLine("self._checkOwner(ctx);");
            } else {
                // Generic modifier: call the check function
                try self.emit("self._check");
                // Capitalize first letter
                if (mod_call.name.len > 0) {
                    var buf: [1]u8 = .{std.ascii.toUpper(mod_call.name[0])};
                    try self.emit(&buf);
                    if (mod_call.name.len > 1) {
                        try self.emit(mod_call.name[1..]);
                    }
                }
                try self.emitLine("(ctx);");
            }
        }

        // Emit transpiled body, capturing output to detect unused params
        if (func.body) |body| {
            const body_start = self.output.items.len;
            try self.emitTranspiledBody(body);
            const body_output = self.output.items[body_start..];
            // Check if self/ctx are used in the emitted body
            const uses_self = std.mem.indexOf(u8, body_output, "self.") != null;
            const uses_ctx = std.mem.indexOf(u8, body_output, "ctx.") != null or
                std.mem.indexOf(u8, body_output, "ctx)") != null or
                std.mem.indexOf(u8, body_output, "ctx,") != null;
            // Insert unused param suppressions BEFORE the body if needed
            if (!uses_self or !uses_ctx) {
                // We need to insert at body_start position
                var suppress_buf: [64]u8 = undefined;
                var suppress_len: usize = 0;
                if (!uses_self) {
                    const s = "    _ = self;\n";
                    @memcpy(suppress_buf[suppress_len..][0..s.len], s);
                    suppress_len += s.len;
                }
                if (!uses_ctx) {
                    const s = "    _ = ctx;\n";
                    @memcpy(suppress_buf[suppress_len..][0..s.len], s);
                    suppress_len += s.len;
                }
                if (suppress_len > 0) {
                    try self.output.insertSlice(self.allocator, body_start, suppress_buf[0..suppress_len]);
                }
            }
        } else {
            // Interface/abstract functions have no body
            if (func.returns.items.len > 0) {
                // Must return a value — emit @panic for unimplemented functions
                try self.emitIndent();
                try self.emitLine("@panic(\"not implemented\");");
            } else {
                try self.emitUnused("self");
                try self.emitUnused("ctx");
            }
        }

        self.indent_level -= 1;
        try self.emitIndent();
        try self.emitLine("}");
    }

    fn generateSpecialFunction(self: *CodeGenerator, name: []const u8, func: parser.FunctionDef) !void {
        try self.emitIndent();
        try self.emit("pub fn ");
        try self.emit(name);
        if (std.mem.eql(u8, name, "fallback")) {
            try self.emitLine("(self: *@This(), ctx: *sdk.ExecutionContext, calldata: []const u8) []const u8 {");
        } else {
            try self.emitLine("(self: *@This(), ctx: *sdk.ExecutionContext) void {");
        }
        self.indent_level += 1;
        if (func.body) |body| {
            try self.emitTranspiledBody(body);
        } else {
            try self.emitUnused("self");
            try self.emitUnused("ctx");
        }
        self.indent_level -= 1;
        try self.emitIndent();
        try self.emitLine("}");
    }

    fn generateStruct(self: *CodeGenerator, s: parser.StructDef) !void {
        try self.emit("pub const ");
        try self.emit(s.name);
        try self.emitLine(" = struct {");
        self.indent_level += 1;
        for (s.fields.items) |field| {
            try self.emitIndent();
            try self.emit(field.name);
            try self.emit(": ");
            try self.emit(type_mapper.mapType(field.type_name));
            try self.emitLine(",");
        }
        self.indent_level -= 1;
        try self.emitLine("};");
        try self.emitLine("");
    }

    fn generateEnum(self: *CodeGenerator, e: parser.EnumDef) !void {
        try self.emit("pub const ");
        try self.emit(e.name);
        try self.emitLine(" = enum(u8) {");
        self.indent_level += 1;
        for (e.members.items) |member| {
            try self.emitIndent();
            try self.emit(member);
            try self.emitLine(",");
        }
        self.indent_level -= 1;
        try self.emitLine("};");
        try self.emitLine("");
    }

    fn generateCustomError(self: *CodeGenerator, err: parser.ErrorDef) !void {
        // Skip duplicate custom errors (can happen when multiple contracts inherit the same errors)
        if (self.emitted_errors.contains(err.name)) return;
        self.emitted_errors.put(err.name, {}) catch {};
        try self.emit("pub const ");
        try self.emit(err.name);
        try self.emit(" = sdk.CustomError(\"");
        try self.emit(err.name);
        try self.emit("\", struct { ");
        for (err.params.items, 0..) |param, i| {
            if (i > 0) try self.emit(", ");
            if (param.name.len > 0) {
                try self.emit(param.name);
            } else {
                try self.emit(std.fmt.allocPrint(self.allocator, "_{d}", .{i}) catch "val");
            }
            try self.emit(": ");
            try self.emit(type_mapper.mapType(param.type_name));
        }
        try self.emitLine(" });");
    }

    // ========================================================================
    // PHASE 2: Transpiled Body Emission (Expression AST → Zig Code)
    // ========================================================================

    fn emitTranspiledBody(self: *CodeGenerator, body: []const u8) !void {
        // Use a scoped arena for all ExprParser allocations (AST nodes, slices).
        // The arena is freed after statement emission, preventing all expr_parser leaks.
        var expr_arena = std.heap.ArenaAllocator.init(self.allocator);
        defer expr_arena.deinit();

        var ep = ExprParser.init(expr_arena.allocator(), body);
        const stmts = ep.parseBlock() catch {
            // Fallback to raw emission with comments
            try self.emitRawBody(body);
            return;
        };
        if (stmts.len == 0) {
            try self.emitUnused("self");
            try self.emitUnused("ctx");
            return;
        }
        for (stmts) |stmt| {
            try self.emitStatement(stmt);
        }
    }

    fn emitStatement(self: *CodeGenerator, stmt: ast.Statement) !void {
        switch (stmt) {
            .variable_decl => |decl| {
                try self.emitIndent();
                // Always emit const unless parser explicitly marks as non-constant
                // AND the variable is known to be reassigned. For safety, default to const.
                try self.emit("const ");
                if (decl.var_names.len == 1) {
                    // Rename if it shadows a contract function
                    if (self.isContractFunction(decl.var_names[0])) {
                        var rename_buf: [128]u8 = undefined;
                        const renamed = std.fmt.bufPrint(&rename_buf, "{s}_local", .{decl.var_names[0]}) catch decl.var_names[0];
                        const renamed_dupe = self.allocator.dupe(u8, renamed) catch decl.var_names[0];
                        try self.param_renames.put(decl.var_names[0], renamed_dupe);
                        try self.emit(renamed_dupe);
                    } else {
                        try self.emit(decl.var_names[0]);
                    }
                } else {
                    // Tuple destructuring not natively supported in Zig — emit as multi-var
                    for (decl.var_names, 0..) |name, i| {
                        if (i > 0) try self.emit(", ");
                        try self.emit(name);
                    }
                }
                if (decl.type_name) |tn| {
                    try self.emit(": ");
                    try self.emit(type_mapper.mapType(tn));
                }
                if (decl.initial_value) |val| {
                    try self.emit(" = ");
                    try self.emitExpr(val);
                } else {
                    try self.emit(" = undefined");
                }
                try self.emitLine(";");
            },
            .expression_stmt => |es| {
                try self.emitIndent();
                try self.emitExpr(es.expr);
                try self.emitLine(";");
            },
            .if_stmt => |s| {
                try self.emitIndent();
                try self.emit("if (");
                try self.emitExpr(s.condition);
                try self.emitLine(") {");
                self.indent_level += 1;
                for (s.then_body) |inner| try self.emitStatement(inner);
                self.indent_level -= 1;
                if (s.else_body) |eb| {
                    try self.emitIndent();
                    try self.emitLine("} else {");
                    self.indent_level += 1;
                    for (eb) |inner| try self.emitStatement(inner);
                    self.indent_level -= 1;
                }
                try self.emitIndent();
                try self.emitLine("}");
            },
            .for_stmt => |s| {
                // Emit init outside loop
                if (s.init) |init_s| try self.emitStatement(init_s);
                try self.emitIndent();
                try self.emit("while (");
                if (s.condition) |c| try self.emitExpr(c) else try self.emit("true");
                try self.emit(") ");
                // Post expression as a continue expression
                if (s.post) |p| {
                    try self.emit(": (");
                    try self.emitExpr(p);
                    try self.emit(") ");
                }
                try self.emitLine("{");
                self.indent_level += 1;
                for (s.body) |inner| try self.emitStatement(inner);
                self.indent_level -= 1;
                try self.emitIndent();
                try self.emitLine("}");
            },
            .while_stmt => |s| {
                try self.emitIndent();
                try self.emit("while (");
                try self.emitExpr(s.condition);
                try self.emitLine(") {");
                self.indent_level += 1;
                for (s.body) |inner| try self.emitStatement(inner);
                self.indent_level -= 1;
                try self.emitIndent();
                try self.emitLine("}");
            },
            .do_while_stmt => |s| {
                try self.emitIndent();
                try self.emitLine("while (true) {");
                self.indent_level += 1;
                for (s.body) |inner| try self.emitStatement(inner);
                try self.emitIndent();
                try self.emit("if (!(");
                try self.emitExpr(s.condition);
                try self.emitLine(")) break;");
                self.indent_level -= 1;
                try self.emitIndent();
                try self.emitLine("}");
            },
            .return_stmt => |s| {
                try self.emitIndent();
                if (s.value) |val| {
                    try self.emit("return ");
                    try self.emitExpr(val);
                    try self.emitLine(";");
                } else {
                    try self.emitLine("return;");
                }
            },
            .emit_stmt => |s| {
                try self.emitIndent();
                try self.emit(s.event_name);
                try self.emit(".emit(ctx, .{");
                for (s.args, 0..) |arg, i| {
                    if (i > 0) try self.emit(", ");
                    try self.emitExpr(arg);
                }
                try self.emitLine("});");
            },
            .revert_stmt => |s| {
                try self.emitIndent();
                if (s.error_name) |name| {
                    try self.emit(name);
                    try self.emit(".raise(.{");
                    for (s.args, 0..) |arg, i| {
                        if (i > 0) try self.emit(", ");
                        try self.emitExpr(arg);
                    }
                    try self.emitLine("});");
                } else if (s.args.len > 0) {
                    try self.emit("sdk.revert(");
                    try self.emitExpr(s.args[0]);
                    try self.emitLine(");");
                } else {
                    try self.emitLine("sdk.revert(\"revert\");");
                }
            },
            .block => |s| {
                try self.emitIndent();
                try self.emitLine("{");
                self.indent_level += 1;
                for (s.statements) |inner| try self.emitStatement(inner);
                self.indent_level -= 1;
                try self.emitIndent();
                try self.emitLine("}");
            },
            .unchecked_block => |s| {
                try self.emitIndent();
                try self.emitLine("// unchecked {");
                try self.emitIndent();
                try self.emitLine("{");
                self.indent_level += 1;
                for (s.body) |inner| try self.emitStatement(inner);
                self.indent_level -= 1;
                try self.emitIndent();
                try self.emitLine("}");
            },
            .try_catch => |s| {
                try self.emitIndent();
                try self.emitLine("// try/catch — mapped to Zig error handling");
                try self.emitIndent();
                try self.emit("if (");
                try self.emitExpr(s.call_expr);
                try self.emitLine(") |result| {");
                self.indent_level += 1;
                try self.emitIndent();
                try self.emitLine("_ = result;");
                for (s.success_body) |inner| try self.emitStatement(inner);
                self.indent_level -= 1;
                for (s.catch_clauses) |clause| {
                    try self.emitIndent();
                    try self.emit("} else |");
                    if (clause.error_name) |name| try self.emit(name) else try self.emit("err");
                    try self.emitLine("| {");
                    self.indent_level += 1;
                    for (clause.body) |inner| try self.emitStatement(inner);
                    self.indent_level -= 1;
                }
                try self.emitIndent();
                try self.emitLine("}");
            },
            .assembly_block => |s| {
                try self.emitIndent();
                try self.emitLine("// assembly { ... } — Yul → Zig SDK mapping");
                // Parse and map common Yul operations to Zig SDK calls
                try self.emitYulBlock(s.raw_code);
            },
            .break_stmt => {
                try self.emitIndent();
                try self.emitLine("break;");
            },
            .continue_stmt => {
                try self.emitIndent();
                try self.emitLine("continue;");
            },
            .placeholder_stmt => {
                try self.emitIndent();
                try self.emitLine("// _; (modifier placeholder — body inserted here)");
            },
            .raw_statement => |s| {
                try self.emitIndent();
                try self.emit("// RAW: ");
                try self.emitLine(s.text);
            },
        }
    }

