    // Expression Emission
    // ========================================================================

    fn emitExpr(self: *CodeGenerator, expr: ast.Expression) !void {
        switch (expr) {
            .literal => |lit| {
                switch (lit.kind) {
                    .number_decimal, .number_hex => {
                        // Convert numeric literals: Solidity numbers → Uint256.fromU64(n) or direct
                        try self.emit("Uint256.fromU64(");
                        try self.emit(lit.value);
                        try self.emit(")");
                    },
                    .string_literal => {
                        try self.emit("\"");
                        try self.emit(lit.value);
                        try self.emit("\"");
                    },
                    .bool_true => try self.emit("true"),
                    .bool_false => try self.emit("false"),
                    .hex_string => {
                        try self.emit("\"\\x");
                        try self.emit(lit.value);
                        try self.emit("\"");
                    },
                    .unicode_string => {
                        try self.emit("\"");
                        try self.emit(lit.value);
                        try self.emit("\"");
                    },
                }
            },
            .identifier => |id| {
                // Map Solidity globals
                if (std.mem.eql(u8, id.name, "msg")) {
                    try self.emit("ctx");
                } else if (std.mem.eql(u8, id.name, "block")) {
                    try self.emit("ctx");
                } else if (std.mem.eql(u8, id.name, "tx")) {
                    try self.emit("ctx");
                } else if (std.mem.eql(u8, id.name, "this")) {
                    try self.emit("self");
                } else if (self.isStateVar(id.name)) {
                    try self.emit("self.");
                    try self.emit(id.name);
                    // Add .load() for StorageSlot-type state vars in read context
                    // Mappings/arrays/strings are accessed via .get()/.set() directly
                    if (!self.emitting_assign_target and self.isSlotStateVar(id.name)) {
                        try self.emit(".load(ctx.storage_backend)");
                    }
                } else if (self.param_renames.get(id.name)) |renamed| {
                    // Apply parameter rename (e.g., owner -> owner_param)
                    try self.emit(renamed);
                } else {
                    try self.emit(id.name);
                }
            },
            .binary_op => |bin| {
                // Map Solidity operators to SDK methods for Uint256
                try self.emitExpr(bin.left);
                switch (bin.op) {
                    .add => try self.emit(".checkedAdd("),
                    .sub => try self.emit(".checkedSub("),
                    .mul => try self.emit(".checkedMul("),
                    .div => try self.emit(".checkedDiv("),
                    .mod => try self.emit(".mod("),
                    .exp => try self.emit(".exp("),
                    .eq => try self.emit(".eql("),
                    .neq => {
                        // Zig can't use != on structs — use !x.eql(y)
                        const needs_method = true; // Address and Uint256 are structs
                        if (needs_method) {
                            try self.emit(".eql(");
                            try self.emitExpr(bin.right);
                            try self.emit(") == false");
                        } else {
                            try self.emit(" != ");
                            try self.emitExpr(bin.right);
                        }
                        return;
                    },
                    .lt => try self.emit(".lt("),
                    .gt => try self.emit(".gt("),
                    .lte => try self.emit(".lte("),
                    .gte => try self.emit(".gte("),
                    .and_ => {
                        try self.emit(" and ");
                        try self.emitExpr(bin.right);
                        return;
                    },
                    .or_ => {
                        try self.emit(" or ");
                        try self.emitExpr(bin.right);
                        return;
                    },
                    .bit_and => try self.emit(".bitwiseAnd("),
                    .bit_or => try self.emit(".bitwiseOr("),
                    .bit_xor => try self.emit(".bitwiseXor("),
                    .shl => try self.emit(".shl("),
                    .shr => try self.emit(".shr("),
                }
                try self.emitExpr(bin.right);
                try self.emit(")");
            },
            .unary_op => |un| {
                if (un.is_prefix) {
                    switch (un.op) {
                        .negate => try self.emit("-"),
                        .not => try self.emit("!"),
                        .bit_not => try self.emit("~"),
                        .increment => {
                            try self.emitExpr(un.operand);
                            try self.emit(" += 1");
                            return;
                        },
                        .decrement => {
                            try self.emitExpr(un.operand);
                            try self.emit(" -= 1");
                            return;
                        },
                        .delete => {
                            try self.emit("// delete ");
                            try self.emitExpr(un.operand);
                            return;
                        },
                    }
                    try self.emitExpr(un.operand);
                } else {
                    // Postfix: emit as statement-level side effect
                    try self.emitExpr(un.operand);
                    switch (un.op) {
                        .increment => try self.emit(" += 1"),
                        .decrement => try self.emit(" -= 1"),
                        else => {},
                    }
                }
            },
            .member_access => |ma| {
                try self.emitExpr(ma.object);
                try self.emit(".");
                // Map Solidity member names to Zig/SDK equivalents
                if (std.mem.eql(u8, ma.member, "sender")) try self.emit("msg_sender") else if (std.mem.eql(u8, ma.member, "value")) try self.emit("msg_value") else if (std.mem.eql(u8, ma.member, "data")) try self.emit("msg_data") else if (std.mem.eql(u8, ma.member, "timestamp")) try self.emit("block_timestamp") else if (std.mem.eql(u8, ma.member, "number")) try self.emit("block_number") else if (std.mem.eql(u8, ma.member, "coinbase")) try self.emit("block_coinbase") else if (std.mem.eql(u8, ma.member, "chainid")) try self.emit("block_chainid") else if (std.mem.eql(u8, ma.member, "origin")) try self.emit("tx_origin") else if (std.mem.eql(u8, ma.member, "gasprice")) try self.emit("tx_gasprice") else if (std.mem.eql(u8, ma.member, "balance")) try self.emit("getBalance()") else if (std.mem.eql(u8, ma.member, "length")) try self.emit("length(ctx.storage_backend)") else try self.emit(ma.member);
            },
            .function_call => |fc| {
                // Special-case require, assert
                if (fc.callee == .identifier) {
                    const name = fc.callee.identifier.name;
                    if (std.mem.eql(u8, name, "require")) {
                        if (fc.args.len >= 2) {
                            try self.emit("sdk.require(");
                            try self.emitExpr(fc.args[0]);
                            try self.emit(", ");
                            try self.emitExpr(fc.args[1]);
                            try self.emit(")");
                        } else if (fc.args.len == 1) {
                            try self.emit("sdk.requireBool(");
                            try self.emitExpr(fc.args[0]);
                            try self.emit(")");
                        }
                        return;
                    }
                    if (std.mem.eql(u8, name, "assert")) {
                        try self.emit("sdk.assert_(");
                        if (fc.args.len > 0) try self.emitExpr(fc.args[0]);
                        try self.emit(")");
                        return;
                    }
                    if (std.mem.eql(u8, name, "keccak256")) {
                        try self.emit("sdk.keccak256(");
                        for (fc.args, 0..) |arg, i| {
                            if (i > 0) try self.emit(", ");
                            try self.emitExpr(arg);
                        }
                        try self.emit(")");
                        return;
                    }
                    if (std.mem.eql(u8, name, "sha256")) {
                        try self.emit("sdk.sha256_hash(");
                        for (fc.args, 0..) |arg, i| {
                            if (i > 0) try self.emit(", ");
                            try self.emitExpr(arg);
                        }
                        try self.emit(")");
                        return;
                    }
                    if (std.mem.eql(u8, name, "ecrecover")) {
                        try self.emit("sdk.ecrecover(");
                        for (fc.args, 0..) |arg, i| {
                            if (i > 0) try self.emit(", ");
                            try self.emitExpr(arg);
                        }
                        try self.emit(")");
                        return;
                    }
                    if (std.mem.eql(u8, name, "addmod")) {
                        try self.emit("sdk.addmod(");
                        for (fc.args, 0..) |arg, i| {
                            if (i > 0) try self.emit(", ");
                            try self.emitExpr(arg);
                        }
                        try self.emit(")");
                        return;
                    }
                    if (std.mem.eql(u8, name, "mulmod")) {
                        try self.emit("sdk.mulmod_fn(");
                        for (fc.args, 0..) |arg, i| {
                            if (i > 0) try self.emit(", ");
                            try self.emitExpr(arg);
                        }
                        try self.emit(")");
                        return;
                    }
                }
                // Check if this is an internal function call (defined in the same contract)
                const is_internal = if (fc.callee == .identifier) blk: {
                    const callee_name = fc.callee.identifier.name;
                    for (self.current_contract_functions) |fn_name| {
                        if (std.mem.eql(u8, callee_name, fn_name)) break :blk true;
                    }
                    break :blk false;
                } else false;

                if (is_internal) {
                    // Internal function calls need self. prefix and ctx as first arg
                    try self.emit("self.");
                    try self.emitExpr(fc.callee);
                    try self.emit("(ctx");
                    for (fc.args) |arg| {
                        try self.emit(", ");
                        try self.emitExpr(arg);
                    }
                    try self.emit(")");
                } else {
                    try self.emitExpr(fc.callee);
                    try self.emit("(");
                    for (fc.args, 0..) |arg, i| {
                        if (i > 0) try self.emit(", ");
                        try self.emitExpr(arg);
                    }
                    try self.emit(")");
                }
            },
            .index_access => |ia| {
                try self.emitExpr(ia.object);
                try self.emit(".get(ctx.storage_backend, ");
                try self.emitExpr(ia.index);
                try self.emit(")");
            },
            .conditional => |cond| {
                try self.emit("if (");
                try self.emitExpr(cond.condition);
                try self.emit(") ");
                try self.emitExpr(cond.true_expr);
                try self.emit(" else ");
                try self.emitExpr(cond.false_expr);
            },
            .assignment => |a| {
                // Determine if this is a storage assignment
                const is_storage_target = self.isStorageTarget(a.target);

                if (is_storage_target) {
                    // Set flag to suppress .load() when emitting the target
                    self.emitting_assign_target = true;

                    // Storage assignment: use .store() / .set() APIs
                    if (a.target == .index_access) {
                        // Target is mapping[key] — use .set(backend, key, value)
                        const ia = a.target.index_access;
                        try self.emitExpr(ia.object);
                        try self.emit(".set(ctx.storage_backend, ");
                        self.emitting_assign_target = false; // key and value are reads
                        try self.emitExpr(ia.index);
                        try self.emit(", ");
                        if (a.op == .assign) {
                            try self.emitExpr(a.value);
                        } else {
                            // Compound: load, modify, store
                            self.emitting_assign_target = true;
                            try self.emitExpr(ia.object);
                            self.emitting_assign_target = false;
                            try self.emit(".get(ctx.storage_backend, ");
                            try self.emitExpr(ia.index);
                            try self.emit(")");
                            try self.emitCompoundOp(a.op);
                            try self.emitExpr(a.value);
                            try self.emit(")");
                        }
                        try self.emit(")");
                    } else {
                        // Target is a state variable — use .store(backend, value)
                        try self.emitExpr(a.target);
                        self.emitting_assign_target = false;
                        try self.emit(".store(ctx.storage_backend, ");
                        if (a.op == .assign) {
                            try self.emitExpr(a.value);
                        } else {
                            // Compound: load, modify, store
                            self.emitting_assign_target = true;
                            try self.emitExpr(a.target);
                            self.emitting_assign_target = false;
                            try self.emit(".load(ctx.storage_backend)");
                            try self.emitCompoundOp(a.op);
                            try self.emitExpr(a.value);
                            try self.emit(")");
                        }
                        try self.emit(")");
                    }
                    self.emitting_assign_target = false;
                } else {
                    // Non-storage assignment: emit normally
                    try self.emitExpr(a.target);
                    if (a.op == .assign) {
                        try self.emit(" = ");
                        try self.emitExpr(a.value);
                    } else {
                        try self.emit(" ");
                        try self.emit(a.op.toString());
                        try self.emit(" ");
                        try self.emitExpr(a.value);
                    }
                }
            },
            .type_cast => |tc| {
                const zig_type = type_mapper.mapType(tc.target_type);
                try self.emit(zig_type);
                if (std.mem.eql(u8, zig_type, "sdk.Address")) {
                    try self.emit(".fromUint(");
                } else if (std.mem.eql(u8, zig_type, "sdk.Uint256") or std.mem.eql(u8, zig_type, "Uint256")) {
                    try self.emit(".fromU64(@intCast(");
                } else {
                    try self.emit("(");
                }
                try self.emitExpr(tc.operand);
                if (std.mem.eql(u8, zig_type, "sdk.Uint256") or std.mem.eql(u8, zig_type, "Uint256")) {
                    try self.emit("))");
                } else {
                    try self.emit(")");
                }
            },
            .new_expression => |ne| {
                try self.emit(ne.type_name);
                try self.emit(".init(");
                for (ne.args, 0..) |arg, i| {
                    if (i > 0) try self.emit(", ");
                    try self.emitExpr(arg);
                }
                try self.emit(")");
            },
            .tuple => |t| {
                try self.emit(".{");
                for (t.elements, 0..) |elem_opt, i| {
                    if (i > 0) try self.emit(", ");
                    if (elem_opt) |elem| {
                        try self.emitExpr(elem);
                    } else {
                        try self.emit("_");
                    }
                }
                try self.emit("}");
            },
            .elementary_type => |et| {
                try self.emit(type_mapper.mapType(et.type_name));
            },
            .array_literal => |al| {
                try self.emit(".{");
                for (al.elements, 0..) |elem, i| {
                    if (i > 0) try self.emit(", ");
                    try self.emitExpr(elem);
                }
                try self.emit("}");
            },
            .state_var_ref => |svr| {
                try self.emit("self.");
                try self.emit(svr.name);
            },
            .delete_expr => |de| {
                try self.emit("// delete ");
                try self.emitExpr(de.operand);
            },
            .emit_expr => |ee| {
                try self.emit(ee.event_name);
                try self.emit(".emit(ctx, .{");
                for (ee.args, 0..) |arg, i| {
                    if (i > 0) try self.emit(", ");
                    try self.emitExpr(arg);
                }
                try self.emit("})");
            },
            .abi_call => |ac| {
                if (std.mem.eql(u8, ac.function, "encode")) {
                    try self.emit("sdk.abi_encode(self.allocator, .{");
                } else if (std.mem.eql(u8, ac.function, "encodePacked")) {
                    try self.emit("sdk.abi_encodePacked(self.allocator, .{");
                } else if (std.mem.eql(u8, ac.function, "encodeWithSelector")) {
                    try self.emit("sdk.abi_encodeWithSelector(self.allocator, ");
                    if (ac.args.len > 0) {
                        try self.emitExpr(ac.args[0]);
                        try self.emit(", .{");
                        for (ac.args[1..], 0..) |arg, i| {
                            if (i > 0) try self.emit(", ");
                            try self.emitExpr(arg);
                        }
                    }
                } else if (std.mem.eql(u8, ac.function, "decode")) {
                    try self.emit("sdk.abi_decode(");
                    for (ac.args, 0..) |arg, i| {
                        if (i > 0) try self.emit(", ");
                        try self.emitExpr(arg);
                    }
                    try self.emit(")");
                    return;
                } else {
                    try self.emit("sdk.abi_encode(self.allocator, .{");
                }
                for (ac.args, 0..) |arg, i| {
                    if (i > 0) try self.emit(", ");
                    try self.emitExpr(arg);
                }
                try self.emit("})");
            },
            .type_info => |ti| {
                if (std.mem.eql(u8, ti.member, "max")) {
                    try self.emit(type_mapper.mapType(ti.target_type));
                    try self.emit(".MAX");
                } else if (std.mem.eql(u8, ti.member, "min")) {
                    try self.emit(type_mapper.mapType(ti.target_type));
                    try self.emit(".MIN");
                } else {
                    try self.emit("@typeInfo(");
                    try self.emit(type_mapper.mapType(ti.target_type));
                    try self.emit(").");
                    try self.emit(ti.member);
                }
            },
        }
    }

