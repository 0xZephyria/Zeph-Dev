    enableJit: bool = false, // Reserved for future RISC-V JIT
    optimizationLevel: OptLevel = .Fast,
    traceExecution: bool = false,
    gasLimit: u64 = 30_000_000,
    /// Execution timeout per contract call (ms)
    executionTimeoutMs: u64 = 5_000,
};

/// Optimization levels for the RISC-V VM execution.
pub const OptLevel = enum {
    None,
    Fast,
    Small,
};

/// Execution context passed from the executor for each block/TX.
/// Provides block-level and transaction-level environment values
/// that contracts can query via syscalls.
/// Execution context passed from the executor for each block/TX.
/// Provides block-level and transaction-level environment values
/// that contracts can query via syscalls.
pub const ExecutionContext = struct {
    /// Block timestamp (seconds since Unix epoch)
    timestamp: u64 = 0,
    /// Transaction originator (tx.origin)
    txOrigin: [20]u8 = [_]u8{0} ** 20,
    /// Transaction gas price
    gasPrice: u64 = 0,
    /// Block coinbase (validator address from consensus)
    coinbase: [20]u8 = [_]u8{0} ** 20,
    /// Block base fee (EIP-1559)
    baseFee: u64 = 0,
    /// Block prevRandao (VRF output from adaptive consensus)
    prevRandao: [32]u8 = [_]u8{0} ** 32,
    /// Block number
    blockNumber: u64 = 0,
    /// Chain ID
    chainId: u64 = 99999,
};

