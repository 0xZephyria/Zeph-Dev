// ============================================================================
// Zephyria — VM Bridge (Production — Thread-Safe)
// ============================================================================
//
// Connects the RISC-V RV32EM VM (forgec/vm/) to the Zephyria node executor
// as the primary smart contract execution backend.
//
// This bridge:
//   • Creates a StateBridge per call (connects VM syscalls → State Overlay)
//   • Passes full execution context (block/tx env) from executor to VM
//   • Propagates logs from HostEnv back to the executor for receipts
//   • Returns contract output (returnData) for external visibility
//
// Thread Safety:
//   The VM operates within the per-TX Overlay isolation model. Each lane in
//   the DAG executor creates its own Overlay. The VMBridge is thread-safe
//   because execution context is passed through the callback's ctx pointer
//   (not global state). Multiple lanes can invoke VM concurrently.
//
// Isolation Model:
//   - Each TX gets its own Overlay (snapshot of world state)
//   - All state changes within a call tree are isolated to that Overlay
//   - On success, the Overlay commits atomically to the world state
//   - Different TXs in different lanes have zero conflicts (guaranteed by DAG scheduler)

const std = @import("std");
const core = @import("core");
const VMCallback = core.dag_executor.VMCallback;
const VMResult = core.dag_executor.VMResult;
const state_mod = core.state;
const state_bridge = @import("state_bridge");
const StateBridge = state_bridge.StateBridge;

/// VMBridge configuration
/// Configuration options for the VMBridge.
