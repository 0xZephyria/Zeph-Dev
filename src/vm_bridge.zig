/// VMBridge provides the integration layer between the Zephyria node executor
/// and the RISC-V VM for contract execution.
///
/// Thread-Safe Design:
///   The VMBridge instance stores the execution context (set per-block).
///   The executeCallback reads context atomically through the ctx pointer.
///   No global mutable state — the bridge pointer is passed through VMCallback.ctx.
/// VMBridge provides the integration layer between the Zephyria node executor
/// and the RISC-V VM for contract execution.
///
/// Thread-Safe Design:
///   The VMBridge instance stores the execution context (set per-block).
///   The executeCallback reads context atomically through the ctx pointer.
///   No global mutable state — the bridge pointer is passed through VMCallback.ctx.
pub const VMBridge = struct {
    allocator: std.mem.Allocator,
    config: VMConfig,
    executionCount: std.atomic.Value(u64),
    totalGasUsed: std.atomic.Value(u64),
    /// Current execution context (set per-block by the miner/block producer)
    /// Read by parallel lanes — set once before block execution, immutable during.
    execCtx: ExecutionContext,

    /// Initializes a new VMBridge instance.
    pub fn init(allocator: std.mem.Allocator, config: VMConfig) VMBridge {
        return .{
            .allocator = allocator,
            .config = config,
            .executionCount = std.atomic.Value(u64).init(0),
            .totalGasUsed = std.atomic.Value(u64).init(0),
            .execCtx = .{},
        };
    }

    /// Deinitializes the VMBridge instance.
    pub fn deinit(self: *VMBridge) void {
        _ = self;
    }

    /// Set execution context for the current block.
    /// Called by the miner/block producer BEFORE block execution starts.
    /// Must not be called while lanes are executing.
    /// Sets the execution context for the current block.
    /// This should be called before block execution begins and remains constant during the block.
    pub fn setExecutionContext(self: *VMBridge, ctx: ExecutionContext) void {
        self.execCtx = ctx;
    }

    /// Returns a VMCallback compatible with both core.executor and core.dag_executor.
    /// The VMBridge pointer is passed through ctx — no global state needed.
    /// Returns a VMCallback compatible with both core.executor and core.dag_executor.
    pub fn getCallback(self: *VMBridge) VMCallback {
        return VMCallback{
            .ctx = @ptrCast(self),
            .exec_fn = &executeCallback,
        };
    }

    /// Returns a legacy VMCallback compatible with core.executor.VMCallback.
    /// Uses the same underlying function but cast to the legacy type.
    /// Returns a legacy VMCallback compatible with core.executor.VMCallback.
    pub fn getLegacyCallback(self: *VMBridge) core.executor.VMCallback {
        return core.executor.VMCallback{
            .ctx = @ptrCast(self),
            .exec_fn = @ptrCast(&executeCallback),
        };
    }

    /// Get execution statistics (thread-safe reads)
    /// Returns thread-safe execution statistics (total count and gas used).
    pub fn getStats(self: *const VMBridge) struct { count: u64, gas: u64 } {
        return .{
            .count = self.executionCount.load(.monotonic),
            .gas = self.totalGasUsed.load(.monotonic),
        };
    }

    /// Execute a contract via RISC-V VM — the main execution callback.
    /// Called by the executor for both contract calls and deployments.
    ///
    /// Thread Safety: The VMBridge pointer is extracted from ctx — no global state.
    /// Each lane passes its own overlay, so there are zero cross-lane data races.
    ///
    /// This function:
    ///   1. Creates a StateBridge connecting VM syscalls → State Overlay
    ///   2. Passes full block/TX execution context
    ///   3. Executes the RISC-V bytecode
    ///   4. Propagates logs from HostEnv to VMResult
    ///   5. Returns output (returnData) for the executor
    fn executeCallback(
        ctx: *anyopaque,
        code: []const u8,
        input: []const u8,
        gas: u64,
        overlay: *state_mod.Overlay,
        caller: core.types.Address,
        value: u256,
    ) VMResult {
        // Thread-safe: extract VMBridge from ctx pointer, no global state
        const bridge: *VMBridge = @ptrCast(@alignCast(ctx));
        _ = bridge.executionCount.fetchAdd(1, .monotonic);

        // Empty code = nothing to execute
        if (code.len == 0) {
            return VMResult{
                .success = true,
                .gasUsed = 0,
                .returnData = &[_]u8{},
            };
        }

        // Create a StateBridge connecting the VM syscalls to the State overlay.
        // The overlay provides per-TX isolation — all state changes are
        // scoped to this overlay until the executor commits or reverts.
        var sb = StateBridge.init(
            bridge.allocator,
            @ptrCast(overlay),
            caller.bytes, // self_address (contract being called)
            caller.bytes, // caller (msg.sender)
            u256ToBytes(value), // call value
            gas,
        );
        defer sb.deinit();

        // Wire full execution context from the block/TX environment
        // These are set once per block and immutable during lane execution
        sb.blockNumber = bridge.execCtx.blockNumber;
        sb.timestamp = bridge.execCtx.timestamp;
        sb.chainId = bridge.execCtx.chainId;
        sb.txOrigin = bridge.execCtx.txOrigin;
        sb.gasPrice = bridge.execCtx.gasPrice;
        sb.coinbase = bridge.execCtx.coinbase;
        sb.baseFee = bridge.execCtx.baseFee;
        sb.prevRandao = bridge.execCtx.prevRandao;

        // Execute via RISC-V VM with all providers wired
        const riscv = @import("vm/riscv/mod.zig");
        const exec_result = riscv.executeContract(
            bridge.allocator,
            code,
            input,
            gas,
            @ptrCast(&sb),
        ) catch {
            return VMResult{
                .success = false,
                .gasUsed = gas,
                .returnData = &[_]u8{},
            };
        };

        _ = bridge.totalGasUsed.fetchAdd(exec_result.gasUsed, .monotonic);

        const ret_buf = if (exec_result.returnData.len > 0)
            bridge.allocator.dupe(u8, exec_result.returnData) catch &[_]u8{}
        else
            &[_]u8{};

        return VMResult{
            .success = exec_result.success,
            .gasUsed = exec_result.gasUsed,
            .returnData = ret_buf,
        };
    }
};

fn u256ToBytes(value: u256) [32]u8 {
    var bytes: [32]u8 = [_]u8{0} ** 32;
    var v = value;
    var i: usize = 31;
    while (v > 0) : (i -= 1) {
        bytes[i] = @truncate(v & 0xFF);
        v >>= 8;
        if (i == 0) break;
    }
    return bytes;
}
